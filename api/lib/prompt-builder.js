// =======================================================================
// api/lib/prompt-builder.js
// -----------------------------------------------------------------------
// /api/ai が使うプロンプトビルダー。mode (result / email) ごとに分離し、
// 業種ヒント・ターゲット状態・AIレベルをプロンプト内に織り込む。
//
// 今回の改善ポイント（営業強化）:
//   - ターゲット状態ごとに「語り口」を明確に切替（manifest/prevention/latent）
//   - risk_message は「期間＋具体現象」必須のルール化
//   - fullレベルのみ「よくある失敗パターン or 構造的ズレ」を差し込み
//   - JSON出力をより安定化（出力スキーマを例示＋禁止事項を強化）
// =======================================================================

'use strict';

const { getIndustryHint } = require('./industry-rules');
const { getLevelGuide, getTargetGuide } = require('./ai-rules');

const AXIS_LABEL_JA = {
  strategy:  '戦略',
  structure: '受入',
  operation: '運用',
  retention: '定着',
};

/* -----------------------------------------------------------------------
   ターゲット状態ごとの「語り口ブロック」
   ---------------------------------------------------------------------
   targetGuide（ai-rules）は短い一言ガイドだが、ここでは
   プロンプト中で複数行にわたって語り口・禁止事項まで明示する。
----------------------------------------------------------------------- */

function getTargetVoiceBlock(target) {
  if (target === 'manifest') {
    return [
      '## 語り口（manifest：現場で課題が顕在化している前提）',
      '- 冒頭で、現場で起きている痛み（離職・定着不全・教育の属人化・評価の曖昧さなど）に共感する',
      '- 「なんとなく」ではなく、すでに起きている可能性が高い問題を具体的に言語化する',
      '- 経営者が見えていない現場の声を翻訳する姿勢を示す',
      '- ただし解決策は出さない。問題の輪郭を鮮明にすることに徹する',
      '- 禁止：「これから〜すべき」「採用前に〜」など予防文脈の言い回し',
    ].join('\n');
  }
  if (target === 'prevention') {
    return [
      '## 語り口（prevention：これから採用する／予防視点）',
      '- 「多くの企業が採用時点の準備不足で失敗している」という業界文脈から入る',
      '- 「採用してから整える」のではなく「採用前に何を定義しておくか」という視点を示す',
      '- 失敗パターン（受入体制がないまま採用して短期離職／現場任せにして崩れる等）を1つ示す',
      '- ただし解決策は出さない。予防視点の「問い」として残す',
      '- 禁止：「すでに起きている」「離職が多い」など顕在課題の断定表現',
    ].join('\n');
  }
  if (target === 'latent') {
    return [
      '## 語り口（latent：今は大きな問題は出ていないが、このまま進むと崩れる前提）',
      '- 「現時点では大きな問題は顕在化していないかもしれない」と一度受け止める',
      '- その上で、属人運用・曖昧な評価・経営と現場のズレなど、数が増えた時に崩れる構造的な危うさを指摘する',
      '- 「今は回っている」ことを逆に違和感の起点として使う',
      '- 人数が増える／経営環境が変わる／キーパーソンが抜けるなど、崩壊トリガーを1つ示す',
      '- ただし解決策は出さない。違和感・危機感・相談余地を残す',
      '- 禁止：「すでに困っている」断定、「準備が全く無い」決めつけ',
    ].join('\n');
  }
  // unknown
  return [
    '## 語り口（情報不足前提）',
    '- 一般論に逃げず、弱い軸を起点に抽象度高めで違和感を指摘する',
    '- 顕在／予防のどちらにも寄りすぎない中立的な書き方にする',
    '- ただし risk_message は軽い表現にせず、弱い軸が放置された場合の経営インパクト（コスト・離職・機能停止など）を具体的に入れる',
  ].join('\n');
}

/* -----------------------------------------------------------------------
   顧客分類（customerSegment）ごとの「語り口ブロック」（2026-06）
   ---------------------------------------------------------------------
   診断冒頭Q1（雇用経験）/ Q2（活用状況）から確定した一次情報のため、
   分類が判明している場合はターゲット状態の語り口より優先して使う。
   分類が unknown / 未指定の場合は null を返し、従来の
   getTargetVoiceBlock(target) にフォールバックする。
----------------------------------------------------------------------- */

const SEGMENT_LABEL_JA = {
  current_well:         '活用中・良好',
  current_some_issues:  '活用中・一部課題あり',
  current_major_issues: '活用中・大きな課題あり',
  past_not_current:     '過去雇用・現在はいない',
  inexperienced:        '未経験',
};

function getSegmentVoiceBlock(segment) {
  if (segment === 'current_well') {
    return [
      '## 語り口（活用中・良好：すでに成果が出ている前提）',
      '- 「すでにうまくいっている」ことを一度肯定的に受け止める',
      '- その上で、属人的な運用のまま増員・横展開した時に崩れる構造的な危うさを指摘する',
      '- テーマは「仕組み化」「制度整備」「再現性」。良い状態を制度に変える視点を示す',
      '- 禁止：「課題が深刻」「失敗している」など現状否定の表現',
      '- 禁止：「これから採用する」など未経験向けの言い回し',
    ].join('\n');
  }
  if (segment === 'current_some_issues') {
    return [
      '## 語り口（活用中・一部課題あり：違和感が出始めている前提）',
      '- 現場コミュニケーションや定着面の「小さな違和感」に共感する',
      '- 小さな違和感を放置すると離職・現場疲弊につながる流れを言語化する',
      '- 「外国人材が悪い」のではなく、受入設計・現場支援の不足に原因がある可能性を示す',
      '- 禁止：「全面的な再設計が必要」など過度に深刻化させる表現',
      '- 禁止：「これから採用する」など未経験向けの言い回し',
    ].join('\n');
  }
  if (segment === 'current_major_issues') {
    return [
      '## 語り口（活用中・大きな課題あり：課題が顕在化している前提）',
      '- 現場で起きている痛み（離職・疲弊・コミュニケーション断絶など）にまず共感する',
      '- 原因は外国人材そのものではなく、受入設計・教育・現場支援の不足にある可能性を明確に示す',
      '- 「まず課題を整理し、受入体制を再設計する」必要性の輪郭を鮮明にする',
      '- 禁止：「うまくいっている」前提の表現、予防文脈の言い回し',
    ].join('\n');
  }
  if (segment === 'past_not_current') {
    return [
      '## 語り口（過去雇用・現在はいない：失敗経験を整理したい前提）',
      '- 過去にうまくいかなかった経験があることを前提に、責めずに受け止める',
      '- 「外国人材が合わなかった」のではなく、職務設計・支援体制・現場理解に改善余地があった可能性を示す',
      '- 失敗原因を整理すれば、再チャレンジすべきかどうかを冷静に判断できるという視点を残す',
      '- 禁止：「現在雇用中」前提の表現、「初めての採用」前提の表現',
    ].join('\n');
  }
  if (segment === 'inexperienced') {
    return [
      '## 語り口（未経験：これから検討する前提）',
      '- 「採用できるか」ではなく「受け入れられる体制があるか」が論点だと示す',
      '- 在留資格・職務設計・教育担当者・生活支援・入社後90日の定着設計など、採用前に確認すべき観点に触れる',
      '- 多くの企業が準備不足のまま採用して短期離職を招く失敗パターンを1つ示す',
      '- 禁止：「すでに起きている」「離職が多い」など顕在課題の断定表現',
    ].join('\n');
  }
  return null; // unknown / 未指定 → 従来のターゲット語り口へフォールバック
}

/* -----------------------------------------------------------------------
   risk_message の強化ルール（共通）
   ---------------------------------------------------------------------
   抽象的な「離職リスクが高まります」を禁止し、
   期間（3〜6ヶ月／半年〜1年 など）＋ 具体現象（早期離職／現場崩壊／
   教育担当の疲弊 等）を必ず含める。
----------------------------------------------------------------------- */

function getRiskMessageRules() {
  return [
    '## risk_message の必須ルール',
    '- 1文で簡潔に（句点で終わる）',
    '- 必ず「期間」と「具体的に起きる現象」を含める',
    '- 期間の例：「3〜6ヶ月以内に」「半年〜1年で」「次の採用サイクルまでに」',
    '- 現象の例：「早期離職が再発する」「教育担当者が疲弊して機能停止する」',
    '  「現場の定着率が目に見えて下がる」「採用投資が回収できないまま消える」',
    '- 禁止表現：「リスクが高まる」「課題が深刻化する」「問題が大きくなる」など抽象一般論',
    '- 禁止表現：「〜しましょう」「〜すべきです」など助言／解決提示',
  ].join('\n');
}

/* -----------------------------------------------------------------------
   full レベルのみに与える追加指示
   ---------------------------------------------------------------------
   light / minimal には付与しない。full だけ情報密度を上げ、
   「よくある失敗パターン」や「構造的なズレ」を1つ織り込む。
----------------------------------------------------------------------- */

function getFullLevelBonus(level) {
  if (level !== 'full') return '';
  return [
    '## full レベル追加指示',
    '- core_issue の中で、この企業に起こりがちな「よくある失敗パターン」または「経営と現場の構造的なズレ」を必ず1つ言語化する',
    '  例：「現場はOJTに頼り続けているのに、経営は定着率だけで判断している」',
    '  例：「評価基準が曖昧なまま人数を増やし、教育担当の負荷が指数的に増える」',
    '- ただし解決策は出さない。構造を指摘するだけに留める',
    '- summary / core_issue / risk_message は情報密度を light より明確に上げる',
  ].join('\n');
}

/* =======================================================================
   result プロンプト
======================================================================= */

function buildResultPrompt(d, level, target) {
  const industry     = d.industry     || '未入力';
  const employees    = d.employees    || '未入力';
  const foreignRatio = d.foreignRatio || '未入力';
  const foreignEmp   = d.foreignEmployed || '未入力';
  const rating       = d.rating       || '—';
  const rate         = d.rate != null ? d.rate + '%' : '—';
  const a            = d.axisRates    || {};

  const axisLines = ['strategy', 'structure', 'operation', 'retention']
    .map((k) => '- ' + AXIS_LABEL_JA[k] + '：' + (a[k] != null ? a[k] + '%' : '—'))
    .join('\n');

  const weakest     = AXIS_LABEL_JA[d.weakestAxis] || '—';
  const guide       = getLevelGuide(level);
  const targetGuide = getTargetGuide(target);
  // 顧客分類が判明していれば分類別の語り口を優先（unknownは従来のターゲット語り口）
  const segBlock    = getSegmentVoiceBlock(d.customerSegment);
  const voiceBlock  = segBlock || getTargetVoiceBlock(target);
  const segLabel    = SEGMENT_LABEL_JA[d.customerSegment] || '';
  const riskRules   = getRiskMessageRules();
  const fullBonus   = getFullLevelBonus(level);
  const indHint     = getIndustryHint(d.industry);

  return [
    'あなたは外国人材活用に精通した日本のBtoBコンサルタントです。',
    '以下の企業の「外国人材活用診断」の結果をもとに、',
    'その会社に向けた診断コメントを日本語で作成してください。',
    '',
    '# 入力情報',
    '- 業種：' + industry,
    '- 従業員数：' + employees,
    '- 外国人比率：' + foreignRatio,
    '- 外国人雇用の有無：' + foreignEmp,
    segLabel ? '- 顧客分類：' + segLabel : '',
    '- 総合スコア：' + (d.score != null ? d.score + ' / 20' : '—'),
    '- 成功確率：' + rate,
    '- 評価ランク：' + rating,
    '- 4軸スコア（達成率%）：',
    axisLines,
    '- 最も弱い軸：' + weakest,
    indHint ? '- 業種補足ヒント：' + indHint : '',
    '',
    '# ターゲット状態：' + target,
    '- 一言方針：' + targetGuide,
    voiceBlock,
    '',
    '# 共通ルール',
    '- その会社に向けた内容にする（テンプレ感を避ける）',
    '- 業種特有の文脈・課題を必ず1つ入れる（業種が「未入力」なら弱い軸を起点に抽象度高めで記述）',
    '- 最も弱い軸を中心に触れる',
    '- 危機感は出すが煽りすぎない',
    '- 解決策・具体策は絶対に出さない（相談余地を残す）',
    '- AIで価値を完結させない。違和感・危機感・相談余地を残すこと',
    '- 文体はBtoB経営者向け。丁寧だが冗長ではない',
    '- トーンは一定に保つ（感嘆符・過剰修辞・カジュアル語禁止）',
    '- ' + guide.tone,
    '- 文字数目安：summary=' + guide.summary + ' / core_issue=' + guide.core_issue + ' / risk_message=' + guide.risk_message,
    '',
    riskRules,
    fullBonus,
    '',
    '# 出力の安定性（必須）',
    '- 出力は必ず下記の JSON オブジェクト1つのみ',
    '- コードフェンス（```）、前置き、後書き、説明文を一切つけない',
    '- キーは summary / core_issue / risk_message の3つのみ・順序も固定',
    '- 値は必ず日本語の文字列（配列・オブジェクト禁止）',
    '- 文中に改行・バッククォート・未エスケープの二重引用符を入れない',
    '- 空文字禁止（3項目すべて必ず埋める）',
    '',
    '# 出力形式（この形だけを返す）',
    '{"summary":"...","core_issue":"...","risk_message":"..."}',
  ].filter(Boolean).join('\n');
}

/* =======================================================================
   email プロンプト（将来拡張・result と同じ語り口ブロックを再利用）
======================================================================= */

function buildEmailPrompt(d, level, target) {
  const weakest     = AXIS_LABEL_JA[d.weakestAxis] || '—';
  const targetGuide = getTargetGuide(target);
  // 顧客分類が判明していれば分類別の語り口を優先（result と同方針）
  const segBlock    = getSegmentVoiceBlock(d.customerSegment);
  const voiceBlock  = segBlock || getTargetVoiceBlock(target);
  const segLabel    = SEGMENT_LABEL_JA[d.customerSegment] || '';
  const indHint     = getIndustryHint(d.industry);

  return [
    'あなたは外国人材活用に精通した日本のBtoBコンサルタントです。',
    '以下の情報をもとに、診断後フォローメールの「件名」と「本文」を作成してください。',
    '',
    '# 入力情報',
    '- 会社名：' + (d.companyName || '—'),
    '- 業種：' + (d.industry || '—'),
    '- 外国人雇用の有無：' + (d.foreignEmployed || '—'),
    segLabel ? '- 顧客分類：' + segLabel : '',
    '- 評価ランク：' + (d.rating || '—'),
    '- 最も弱い軸：' + weakest,
    indHint ? '- 業種補足ヒント：' + indHint : '',
    '',
    '# ターゲット状態：' + target,
    '- 一言方針：' + targetGuide,
    voiceBlock,
    '',
    '# 共通ルール',
    '- BtoB経営者向けの丁寧なトーン',
    '- 診断結果への言及 → 業種特有の課題 → 弱い軸の示唆 → 60分無料相談への導線',
    '- 解決策・具体策は出さない',
    '- 本文は350〜500字',
    '',
    '# 出力の安定性',
    '- 出力は下記の JSON オブジェクト1つのみ',
    '- コードフェンス・前置き・後書き禁止',
    '- キーは subject / body のみ',
    '',
    '# 出力形式',
    '{"subject":"...","body":"..."}',
  ].filter(Boolean).join('\n');
}

module.exports = {
  buildResultPrompt,
  buildEmailPrompt,
};
